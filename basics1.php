    <dd>CH is the short hand notation for chaing that is found in most patterns. The notations will pop up throughout the projects.
      Once you are holding the yarn in a comfortable way you will lay the hook on the yarn with the hook facing down. Pull the hook down and
      towards yourself and back up, looping the yarn on the hook. *Pinch the bottom of the loop where the yarn overlaps its self and reach your hook
      up to pick up the yarn that runs over your pointer finger. Turn the hook down and pull that yarn through the loop you are pinching. Turn the
      hook so it is face up again. Repeat from the * till you have as many stitches as you need.</dd></br>
    <dd>Note: Don't pull them to tight on your hook or you won't be abe to work in them.</dd>
