    <dt>Learning the Basics</dt>
    <dd>It might seem like there is an insane amount of things that you would
      need to learn before you could try any project. That's not the case! What you need to know is
      how to do just the basic stitches. We will go over all of them right here. If you need a refresher on
      how to do them later you can always come back and look over them again.
    </dd></br>
    <dd>Before you start you will need to pick your yarn and your hook. The sie of the yarn will determine the size of the hook that is needed.
      The thicker that the yarn is the bigger the hook you will need. Most patterns will not give you a specific size yarn to use they will only
      give you an amount of yarn. That is usually done by weight. Most patterns though are done with just normal thickness yarn. The suggested
      size hook to work with this yarn is a 5.5mm hook. You can really use anysize hook that you want. Just use what you think will be comfortable
      for you to hold.</dd>
