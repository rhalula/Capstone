    <dd>To create a granny square, you will need to know how to do one other piece that hasn't been covered yet called a slip stitch (SL ST). A slip stitch is used
      to close rounds or to move without leaving spaces or decreasing the number of stitches you have. To create a slip stitch insert your hook into the stitch
      where you are supposed to be making it, pick up the yarn and pull it through both loops on your hook.
    </dd>
