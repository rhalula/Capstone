  <dl>
    <dd>To start a granny square, you are going to chain 5 and make a slip stitch into the first chain creating a loop. You will then chain 3 and make 2 double crochets
      into the center or your ring. You will then *chain 2 and make 3 double crochets into the center. Repeat * 2 more times (a total of 3 times). Chain 2 more and make a slip
      stitch to join to the beginning of the round. That is the first round complete! To go to the next round you will make slip stitches over to the corner space. From there
      you will chain 3 and make 2 more double crochets in that corner space. Chain 2 and make 3 double crochets into that same corner you were just working in. This will create
      your first corner. You will then *chain 2 and make 3 double crochets, chain 2 and make 3 more double crochets in the same space. You will repeat the * section 2 more times.
      Chain 2 and slip stitch to join the round. Again slip stitch over to the corner. This is when the pattern for the rest of a granny square can be laid out. Once you are in a
      corner, chain 3, make 2 double crochets, chain 2, and make 3 more double crochets in that same space. This is the pattern for all the starting corners for the rest of the square
      (no matter how big you make it). To work a side, you will chain 2, work 3 double crochets into the space(s). If there is more than 1 space on a side chain 2 between each set of
      double crochets. To make the corners, you will chain 2 to get over to a corner, make 3 double crochets, chain 2 more and make 3 more double crochets in the same spot. Repeat the
      side and corner parts until you make it back to where you started. That is the basic pattern that all the round of a granny will follow.
    </dd>
