    <dd>To start out, you will need to chain 35 stitches. You will make a single crochet in the third stitch from your hook. Then you will chain 1, skip over the next stitch and
      single crochet in the next(Single crochet in the second stitch from where you made your last single crochet). Repeat that set until you have reached the end of the row.
      You are looking to have 33 stitches by the time you finish the row. That will include single crochets and chains. Chain 1 and turn your work over. This next part will be
      what you do for the rest of the square. **Single crochet into the first single crochet, and you will skip the first chain. *Single crochet in the next single crochet and chain 1.
      Repeat * until you have finished the row chain 1 and turn. You will then repeat ** until you are about a square. Tie off your work.
