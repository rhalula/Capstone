  <dl>
    <dd>For this one we are going to make a hexagon but you can really make almost any shape one that you want. To start you are going to chain 4 and make a slip stitch
      into to first stitch to create a ring. Chain 2 then make 12 double crochets into the center of the ring and close the round with a slip stitch. Chain 4 (which will
      count as a double crochet and a chain). Skip the next stitch. You are then going to make 2 double crochets, a chain, and 2 more double crochets into every other stitch
      the rest of the way around. Before you finish this round you need to finish the first corner. You will make a double crochet in the base of the chains and make a
      slip stitch into the top of the chains to finish that round. This is the point that the pattern for the rest can be laid out. To start you will chain 3, make 2 double
      crochets and 1 more chain. at this point you will move to the corner. To make a corner you will make 2 double crochets, chain 2, and 2 more double crochets. That will finish
      a corner but you will need 1 more chain to move to a side. Making a side is 3 double crochets in a side hole and chain to get to either another side whole or corner.
      To finish a round, you will just make slip stitches over to the next hole.
