    <dd>With a half double crochet you will start out similar to a single crochet. We will start by chaning 12 stitches. The last 2 will be our
      turning stitches. turn your work over and we will start making a half ouble crochet by putting the yarn over the hook and inserting it into
      the thirst stitch from out hook. Pull the yarn over and pull through the first loop. At this point you should have 3 loops on your hook.
      Pull the yarn over again this time pull it through all 3 loops. Continue until you reach the end and have 10 stitches. Chain 2, turn your
      work over and keep practicing untill you are comfortable.
