    <dd>With a double crochet we will start by chaining 12 to make our piece 10 wide. This will seem very similar to a half double crochet but
      they are different. To start we will pull the yarn over and insert it into the thirst stitch from the hook. Pull the yarn over again and
      pull it through the first loop. This is where the half double and double crochets differ. As opposed to pulling it you pulling the yarn over and
      through all three loops, we are going to pull the yarn over and only pull it through the first 2 loops on the hook. You should have 2 loops
      on your hook. Pull the yarn over again and pull it through the last 2 loops. Continue till you have 10 stitches. Chain 3, turn your work over
      and keep practicing till you are comfortable.</dd>
  </dl>
