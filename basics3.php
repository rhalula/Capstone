  <dd>Triple crochets are the tallest stitch that is used. To make a 10 stitch wide piece, we need to chain 13. This time we are going to wrap
    our yarn around the hook twice. Then we will insert the hook into the forth(?) stitch from our hook. Pick up the yarn and pull it through
    the first loop so that we have 4 loops on our hook. Pull the yarn over and pull it through the first 2 stitches on the hook so that you have
    3 left. Pull the yarn over again and pull it through the next 2 stitches so you have 2 stitches left on your hook. Finally, pull the yarn over
    and pull it through the last 2 stitches. Continue until you have 10 stitches. Chain 4, turn your work over and keep practicing till you are
    comfortable.</dd></br>
  <dd>This is when you will tie off if you have been making one piece with all the stitches. As I said earlier, cut the yarn a few inches from your
    hook and pull it all the way through. Make sure that you pull it tight.</dd>
