    <dt>2-D to 3-D</dt>
    <dd>Going from 2-D to 3-D is not as hard or scary as it seems. The main thing
      is that you don't have enough stitches around to keep going out.
    </dd>
  </dl></br>
  <dl>
    <dd>So there really isn't a way to show moving grom 2-D to 3-D other than creating a bowl, which we will do next. So right now I am just gonna talk about what actually happens when you switch.
      While it might sound difficult to move from 2-D to 3-D, it is actuallypretty easy. The easiest way to explain is that the amount of outward expansion needed to stay in 2-D, wasn't met.
      This just means, there needs to be a few more stitches to stay 2-D. Making a small bowl will start out looking very similar to making a circle, which is why we went over that before
      going over this.
    </dd></br>
    <dd>Now there is one major difference between the circle and the bowl. The bowl doesn't end each row by slip stitch into the chaining. In fact, it doesn't have a chain. This is because
      the bowl is created by crocheting in the round. This means the project looks more like a spiral than concentric circles.
  </dl>
