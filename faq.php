    <h2>FAQ</h2>
    <dl>
      <dt>Why were these projects chosen?</dt>
      <dd>- Each one of the smaller projects reinforces an aspect that is used to create the bigger projects. As for why were the 2 starting projects a bag and a blanket?
        These are two projects that act as stepping stones to creating most anything through crochet. The blanket is a big project that is relatively simple and repedative,
        While the bag is a bit more complex and focuses on work in 3-D as opposed to 2-D.</dd>
      <dt>Where can I get yarn and a hook?</dt>
      <dd>- Yarn and hooks are sold just about any where that has a craft section. If you are just starting out,
      you don't need to spend a ton on yarn.</dd>
      <dt>Does it matter what type of yarn I get?</dt>
      <dd>- It depends what you are making. If you are making a blanket, I would suggest getting the yarn that is the softest
        to you. If you are making a washcloth, I would suggest getting a cotton yarn which will absorb water in a similar
        matter to a store bought washcloth.<dd>
      <dt>Does the color matter?</dt>
      <dd>- Not at all! Pick the color or colors that you like best. I am using colors that I already had on hand. But starting
        with a lighter color, it will be easier to see what you are doing. When I say lighter colors, I don't mean that you need
        to use white, I am just suggesting that you avoid black, dark brown, or other dark colors.</dt>
      <dt>What size hook do I need to get?</dt>
      <dd>- Differnt projects will call for different sized hooks but most can be done with whatever hook you find or
      buy. A 4.5mm or a 5.0mm is the common size that many projects suggest but use the hook that feels best for you.</dd>
      <dt>How much yarn do I need?</dt>
      <dd>- That depends on the project and the thickness of the yarn. The thicker the yarn the more that will be needed.
      The size of the project will dictate how much yarn you will need.</dd>
      <dt>My project is too tight to get my hook in. How do I fix this?</dt>
      <dd>- You might need a bigger hook. This is a common problem that many crocheters run into. The other option if you
        don't want to buy a new hook is to try not pulling your work as tight. This does come with practice on controlling
        the tension.</dd>
    </dl>
