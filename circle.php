  <dl>
    <dd>To start out, chain 5 and make a slip stitch into the first one to make a circle. Chain 3 and make 12 double crochets into the center of the circle. Make a slip stitch to
      complete the round. For the next round chain 3 and make 2 double crochets in each stitch around, and make a slip stitch to complete the round. For the next round, chain
      3 and make 2 double crochets in the next stitch. For the rest of the round make 1 double crochet in the next stitch then 2 ouble crochets in the following. To continue just add
      1 more double crochet before the 2 double crochets. So the next round would be 2 dc, 1 dc in next 2 stitches, then 2 dc in the next.
    </dd>
