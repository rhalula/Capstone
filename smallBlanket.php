    <dd>To make a blanket, we aren't just going to make a big square we are going to make a zig zag blanket. In order to do this, we will need to learn how to make a decreasing stitch. This is
      commonly called a decrease # together(DC#TOG). In this pattern we will use both a dc2tog and a dc3tog. Making these are relatively simple. You will wrap the yarn over, insert your
      hook into the stitch, pick up the yarn, pull it through the first loop, pull the yarn over and pull it through the next 2 loops. When you have 2 loops left on your hook, you will pull the
      yarn over again, insert it into the next stitch and again pull the yar through the first loop, pull the yarn over and pull it through the next 2 loops. At this point you should have 3 loops
      on your hook. If you are making a dc2tog, you will pull the yarn over one last time and pull it through all 3 loops. If you are making a dc3tog, you will do a similar thing that you have
      already done, yarn over, insert,pull yarn through, pull yarn through next 2. At this point you will have 4 loops on your hook. To complete a dc3tog you will pull the yarn over and pull it through
      the 4 loops.
    </dd></br>
    <dd>So now that we have all the parts that we need to make the blanket, we start by chaining 114. If you are having trouble keeping track of what stitch you are on, you can use paperclips to
      mark out a set number of stitches. I find it is helpful to do it in sets of 10 stitches. Make a double crochet into the third stitch from the hook. Make 1 double crochet in the next 6 chains.
      make 3 double crochets in the next chain, 1 double crochet in the next 6 chains. *Make a decrease 3 together(dc3tog) over the next 3 stitches, 1 double crochet in the next 6 chains, 3
      double crochets in the next stitch, and 1 double crochet in the next 6. Repeat * across to last 2 stitches. Make a decrease 2 together(dc2tog) over the last 2. This next set will be the
      pattern for the rest of the blanket. **Chain 3 and turn your work over. Skip the first stitch and make 1 double crochet in the next 7 stitches. Make 3 double crochets in the next stitch and
      1 double crochet in the next 6 stitches. *Make a decrease 3 together(dc3tog) over the next 3 stitches, 1 double crochet in the next 6 chains, 3 double crochets in the next stitch, and 1
      double crochet in the next 6. Repeat * across to the last 2 stitches. Make a decrease 2 together(dc2tog) over the last 2. Repeat the ** section untill you have reached your desired length.
