    <dd>Counting Stitches: I have mentioned counting stitches to make sure that you have the right amount. Here I will explain how to actually
      do this. To count stitches, Look at the top edge of your work. Every chain stitch is 1 stitch. When I say to put your hook in the second
      stitch from your hook, you dont count the loop that is around your hook at that moment. You will only count the completed ones.</dd></br>
    <dd>Note: When you are learning, it is a good idea to count at the end of each row to make sure that you didn't skip any or double up on any.</dd></br>
    <dd>Tension: One thing that even people who have been crocheting for a while sometimes struggle with is the tension or tightness of their work.
      The main problem is their work being too tight. To keep this from happening, hold your work near or at the bottom of the stitch you are working
      on. Also keep an eye on how tight the yarn you are pulling is. This can also mess with the tension.</dd>
