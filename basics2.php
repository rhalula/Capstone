    <dd>Chain 11 stitches (yes, you will need to chain 1 more than we are making our block. The extra one is called a turn stitch). The first
      single crochet will go in the second stitch from the hook. Push your hook into the stitch and pick up the yarn pulling it through the first
      loop. You should have 2 loops on your hook now. Pick up the yarn again and this time pull it through both loops on your hook. You should now
      have 1 loop left on your hook. Repeat in the rest of the stitches across your piece.</dd></br>
    <dd>The last stitch can be difficult to spot especially if you made the first chain tight. To fix that simply loosen it a bit and make
      the stitch. At the end of every row, you will make what is called a turning stitch. This stitch or stitches brings you up to the next row
      so that you can continue working. Different stitches will require a different amount of chains. For a single crochet, the turn stitch is
      1 chain. you will always want to work in the same direction and the turning stitch allows you to do this. For right handed people you will
      work from right to left and left handed people will work left the right.</dd></br>
    <dd>When doing another row of single crochet, you will still put your hook in the second stitch, the only difference this time is that it
      will be the first single crochet of the row below. Do the same thing as you did on the last row making singe crochets all the way across
      until you reach the end. Work as many rows as you would like doing this untill you feel comfortable with it. To tie off, After you have
      completed your last single crochet, don't make a turning stitch. Cut the yarn a few inches from your hook and pull it all the way through
      making sure you pull it tight.</dd>
  </dl>
