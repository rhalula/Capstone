    <dt>Learning the Basics Part 2</dt>
    <dd>So there is more to creating something than just chaining so we will go over the basic stitches that we will need to make just about any
      project that we could want to make!
    </dd></br>
    <dd>We are going to make a small sample of these next few stitches. You can either make one piece that has all of them together or you can make
      multiple smaller ones. it is up to you. If you are making one piece then when I say chain 10, you can ignore that bit but if the directions say
      chain 11 or 12, you will need to chain the extra stitches to make sure that you have the right number of stitches.</dd>
  </br>
    <dd>Note: the notations in parentesis at the end of the names are the common notations that are used in patterns. These are all done in American
      notations so if you are working on a project that uses a different notation, you will need to remember what their notations are.</dd></br>
